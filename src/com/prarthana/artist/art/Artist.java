package com.prarthana.artist.art;

/**
 *
 * @author Pavilion G4
 */
public class Artist {
    private int year;
    private String artistName,album,title;
    private boolean status;

    public Artist() {
    }

    public Artist(int year, String artistName, String album, String title, boolean status) {
        this.year = year;
        this.artistName = artistName;
        this.album = album;
        this.title = title;
        this.status = status;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public String getArtistName() {
        return artistName;
    }

    public void setArtistName(String artistName) {
        this.artistName = artistName;
    }

    public String getAlbum() {
        return album;
    }

    public void setAlbum(String album) {
        this.album = album;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public boolean isStatus() {
        return status;
    }

    public void setStatus(boolean status) {
        this.status = status;
    }
    
    
    
    
}
